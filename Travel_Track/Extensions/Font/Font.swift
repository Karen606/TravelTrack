import UIKit

extension UIFont {
    static func castoroTitling(size: CFloat) -> UIFont? {
        return UIFont(name: "CastoroTitling-Regular", size: CGFloat(size))
    }
    
    static func architects(size: CFloat) -> UIFont? {
        return UIFont(name: "ArchitectsDaughter", size: CGFloat(size))
    }
}
